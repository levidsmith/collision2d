using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class Line  {
    public int x1;
    public int y1;
    public int x2;
    public int y2;

    public Line(int inX1, int inY1, int inX2, int inY2) {
        x1 = inX1;
        x2 = inX2;
        y1 = inY1;
        y2 = inY2;
    }
}